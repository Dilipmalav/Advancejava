package in.co.rays.marksheet;

public class MarksheetBean {
	
	private int id;
	private String name;
	private int ro_no;
	private int che;
	private int phy;
	private int maths;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRo_no() {
		return ro_no;
	}
	public void setRo_no(int ro_no) {
		this.ro_no = ro_no;
	}
	public int getChe() {
		return che;
	}
	public void setChe(int che) {
		this.che = che;
	}
	public int getPhy() {
		return phy;
	}
	public void setPhy(int phy) {
		this.phy = phy;
	}
	public int getMaths() {
		return maths;
	}
	public void setMaths(int maths) {
		this.maths = maths;
	}
	
	
	

}
